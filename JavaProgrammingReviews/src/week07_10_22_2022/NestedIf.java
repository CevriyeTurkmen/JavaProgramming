package week07_10_22_2022;

public class NestedIf {
    public static void main(String[] args) {
        int starcolumn = 7;
        int starrow = 6;

        for (int  i= 1;  i<= starcolumn; i++) {
            for (int j= 0;  j<=starrow ; j++) {
                System.out.print("*");
            }System.out.println();
        }



















    }
}
